/**
 * Рекурсивное вычисление суммы элементов массива
 * Использует вектор для хранения данных и рекурсивный подход
 * Базовый случай: индекс достигает размера массива
 * Рекурсивный случай: элемент + сумма хвоста массива
 * Временная сложность: O(n), где n - количество элементов
 */

#include <iostream>
#include <vector>
using namespace std;

int array_sum(vector<int>& arr, int index = 0) {
    // Базовый случай: дошли до конца массива
    if (index == arr.size()) {
        return 0;
    }
    // Рекурсивный случай: текущий элемент + сумма оставшихся
    return arr[index] + array_sum(arr, index + 1);
}

int main() {
    vector<int> arr = {1, 2, 3, 4, 5};
    cout << "Сумма массива = " << array_sum(arr) << endl;
    return 0;
}

// Вывод:
// Сумма массива = 15
