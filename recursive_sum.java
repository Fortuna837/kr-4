/**
 * Рекурсивное вычисление суммы элементов массива
 * Класс содержит статический метод для рекурсивного суммирования
 * Базовый случай: индекс равен длине массива
 * Рекурсивный случай: поэлементное сложение с рекурсивным вызовом
 * Временная сложность: O(n), где n - количество элементов
 */

public class ArraySum {
    public static int arraySum(int[] arr, int index) {
        // Базовый случай: дошли до конца массива
        if (index == arr.length) {
            return 0;
        }
        // Рекурсивный случай: текущий элемент + сумма оставшихся
        return arr[index] + arraySum(arr, index + 1);
    }
    
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        System.out.println("Сумма массива = " + arraySum(arr, 0));
    }
}

// Вывод:
// Сумма массива = 15
